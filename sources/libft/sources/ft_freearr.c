#include "../libft.h"

void	ft_freearr(char **arr)
{
	int	i;

	if (!arr)
		return ;
	if (!(*arr))
		return (free(arr));
	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	free(arr);
}
